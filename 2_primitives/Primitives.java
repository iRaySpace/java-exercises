public class Primitives {
    public static void main(String[] args) {
        int ten; // set to 10
        System.out.println("10"); // replace to `ten`
        
        float tenFloat; // set and cast `ten` to float
        System.out.println("10.0"); // replace to tenFloat

        double tenDouble; // set and cast `tenFloat` to double 
        System.out.println("10.0"); // replace to tenDouble

        int fifteen = 5; // set and cast `tenDouble` to int and add 10
        System.out.println("15"); // replace to fifteen
    }
}
